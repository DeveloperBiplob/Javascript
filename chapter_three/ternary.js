//----------------- Ternary Operator-----------------//
var n = 11 
var str = ''

if(n % 2 == 0){
    str = 'EVEN'
}else{
    str = 'ODD'
}
console.log(str)



// Condition ? true side : false side

var result = n % 2 == 0 ? 'EVEN' : 'ODD'
console.log(result)

console.log( n % 2 == 0 ? 'EVEN' : 'ODD' )